#include "libft.h"

int	ft_atoi(const char *str)
{
	int					i;
	int					s;
	unsigned long long	r;

	i = 0;
	s = 1;
	r = 0;
	while ((str[i] == 32) || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = s * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' )
	{
		if (r >= 922337203685477580 && str[i] - '0' > 7 && s == 1)
			return (-1);
		if (r >= 922337203685477580 && str[i] - '0' > 8 && s == -1)
			return (0);
		r = r * 10 + (str[i] - '0');
		i++;
	}
	return (r * s);
}
