#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	s_len;
	int		i;
	char	*a;

	if (s == NULL || f == NULL)
		return (NULL);
	s_len = ft_strlen(s);
	a = malloc(s_len + 1);
	if (!a)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		a[i] = f(i, s[i]);
		i++;
	}
	a[i] = '\0';
	return (a);
}
